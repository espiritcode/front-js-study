// 1~20까지 숫자중 3의 배수만 출력
// continue 사용 

// 3 % 3 === 0
// 6 % 3 === 0
// 9 % 3 === 0
// 12 % 3 === 0
// 15 % 3 === 0
// 18 % 3 === 0

for(let i = 0; i < 20; i++){
    let num = i + 1
    if(num % 3 !== 0){
        // num % 3 !== 0: 3의 배수가 아닐 때
        continue;
    }
    console.log(num)
}
